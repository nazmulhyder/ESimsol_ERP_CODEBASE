        public byte[] PrepareReport(List<ExportPIReport> oExportPIReports, Company oCompany, string sDateRange)
        {
            _oExportPIReports = oExportPIReports.OrderBy(x => x.BankName).ToList();
            _oCompany = oCompany;

            _sDateRange = sDateRange;

            #region Page Setup
            _oDocument = new Document(new iTextSharp.text.Rectangle(1200, 650), 0f, 0f, 0f, 0f);
            //_oDocument.SetPageSize(iTextSharp.text.PageSize.A4);
            //_oDocument.SetPageSize(new iTextSharp.text.Rectangle(350, 230));
            _oDocument.SetMargins(40f, 40f, 5f, 40f);
            _oPdfPTable.WidthPercentage = 100;
            _oPdfPTable.HorizontalAlignment = Element.ALIGN_LEFT;

            _oFontStyle = FontFactory.GetFont("Tahoma", 8f, 1);
            PdfWriter.GetInstance(_oDocument, _oMemoryStream);
            _oDocument.Open();
            _oPdfPTable.SetWidths(new float[] { 12f, 30f, 53f, 50f, 50f, 50f, 50f, 40f, 40f, 50f, 50f, 50f });
            #endregion

            this.PrintHeader();
            this.PrintBody();
            //this.PrintFooter();
            _oPdfPTable.HeaderRows = 5;
            //_oPdfPTable.FooterRows = 3;
            _oDocument.Add(_oPdfPTable);
            _oDocument.Close();
            return _oMemoryStream.ToArray();
        }

