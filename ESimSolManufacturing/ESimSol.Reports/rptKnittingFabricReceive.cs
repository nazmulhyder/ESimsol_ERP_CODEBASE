        public byte[] PrepareReport(KnittingFabricReceive oKnittingFabricReceive, Company oCompany, BusinessUnit oBusinessUnit, List<ApprovalHead> oApprovalHeads, List<ApprovalHistory> oApprovalHistorys)
        {
            _oCompany = oCompany;
            _oKnittingFabricReceive = oKnittingFabricReceive;
            _oKnittingFabricReceiveDetails = oKnittingFabricReceive.KnittingFabricReceiveDetails;
            _oBusinessUnit = oBusinessUnit;
            _oApprovalHeads = oApprovalHeads;
            _oApprovalHistorys = oApprovalHistorys;

            #region Page Setup
            _oDocument = new Document(PageSize.A4, 0f, 0f, 0f, 0f);
            _oDocument.SetMargins(30f, 30f, 5f, 30f);
            _oPdfPTable.WidthPercentage = 100;
            _oPdfPTable.HorizontalAlignment = Element.ALIGN_LEFT;
            _oFontStyle = FontFactory.GetFont("Tahoma", 15f, 1);
            PdfWriter.GetInstance(_oDocument, _oMemoryStream);
            _oDocument.Open();
            _oPdfPTable.SetWidths(new float[] { 595 });
            #endregion
            this.PrintEmptyRow();
            this.PrintHeader();
            this.PrintEmptyRow();
            this.PrintKnittingFabricReceiveHeader("Knitting Fabric Receive");
            this.PrintEmptyRow();
            this.PrintKnitFabricRcv();
            this.PrintEmptyRow();
            this.PrintKnitFabricRcvDetails();
            this.PrintEmptyRow();
            this.PrintEmptyRow();
            this.PrintEmptyRow();
            this.PrintSignature();
            _oPdfPTable.HeaderRows = 4;
            _oDocument.Add(_oPdfPTable);
            _oDocument.Close();
            return _oMemoryStream.ToArray();
        }

