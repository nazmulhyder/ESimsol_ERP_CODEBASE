        #region Report Body
        private void PrintBody()
        {
            _oFontStyle = FontFactory.GetFont("Tahoma", 8f, iTextSharp.text.Font.BOLD);

            if (_oAttendanceDailys.Count <= 0)
            {
                _oFontStyle = FontFactory.GetFont("Tahoma", 12f, iTextSharp.text.Font.BOLD, iTextSharp.text.BaseColor.RED);
                _oPdfPCell = new PdfPCell(new Phrase("There is no data to print !!", _oFontStyle)); _oPdfPCell.Colspan = 9; _oPdfPCell.Border = 0;
                _oPdfPCell.HorizontalAlignment = Element.ALIGN_CENTER; _oPdfPCell.BackgroundColor = BaseColor.WHITE; _oPdfPTable.AddCell(_oPdfPCell);
                _oPdfPTable.CompleteRow();

            }
            else
            {
                _oAttendanceDailys = _oAttendanceDailys.OrderBy(x => x.DepartmentName).ToList();
                while (_oAttendanceDailys.Count > 0)
                {
                    List<AttendanceDaily> oTempAttendanceDailys = new List<AttendanceDaily>();
                    oTempAttendanceDailys = _oAttendanceDailys.Where(x => x.DepartmentName == _oAttendanceDailys[0].DepartmentName).OrderBy(x => x.EmployeeCode).ToList();
                    //oTempDAs.AddRange(oTempAttendanceDailys);
                    PrintAttendanceDaily(oTempAttendanceDailys);
                    _oAttendanceDailys.RemoveAll(x => x.DepartmentName == oTempAttendanceDailys[0].DepartmentName);
                }
            }

            _oPdfPCell = new PdfPCell(new Phrase(" ", _oFontStyle)); _oPdfPCell.Colspan = 9; _oPdfPCell.Border = 0; _oPdfPCell.FixedHeight = 30;
            _oPdfPCell.HorizontalAlignment = Element.ALIGN_CENTER; _oPdfPCell.BackgroundColor = BaseColor.WHITE; _oPdfPTable.AddCell(_oPdfPCell);

            _oPdfPTable.CompleteRow();
        }
