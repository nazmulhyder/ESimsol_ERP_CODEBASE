        public byte[] PrepareReport(AttendanceDaily oAttendanceDaily)
        {
            _oAttendanceDailys = oAttendanceDaily.AttendanceDailys;
            _oCompany = oAttendanceDaily.Company;

            if (_oAttendanceDailys.Count > 0)
            {
                if (oAttendanceDaily.ErrorMessage == "Present")
                {
                    sHeader = "PRESENT EMPLOYEES OF " + _oAttendanceDailys[0].AttendanceDateInString;
                }

                else if (oAttendanceDaily.ErrorMessage == "Absent")
                {
                    sHeader = "ABSENT EMPLOYEES OF " + _oAttendanceDailys[0].AttendanceDateInString;
                }

                else if (oAttendanceDaily.ErrorMessage == "Late")
                {
                    sHeader = "LATE OF " + _oAttendanceDailys[0].AttendanceDateInString;
                }

                else if (oAttendanceDaily.ErrorMessage == "Early")
                {
                    sHeader = "EARLY LEAVING OF " + _oAttendanceDailys[0].AttendanceDateInString;
                }
                else
                {
                    sHeader = "Date : " + _oAttendanceDailys[0].AttendanceDateInString;
                }
            }
            #region Page Setup
            _oDocument = new Document(PageSize.A4, 0f, 0f, 0f, 0f);
            _oDocument.SetPageSize(iTextSharp.text.PageSize.A4);
            //_oDocument.SetPageSize(new iTextSharp.text.Rectangle(350, 230));
            _oDocument.SetMargins(40f, 40f, 5f, 40f);
            _oPdfPTable.WidthPercentage = 100;
            _oPdfPTable.HorizontalAlignment = Element.ALIGN_LEFT;

            _oFontStyle = FontFactory.GetFont("Tahoma", 8f, 1);
            PdfWriter.GetInstance(_oDocument, _oMemoryStream);
            _oDocument.Open();
            _oPdfPTable.SetWidths(new float[] { 20f, 70f, 90f, 100f, 45f, 50f, 50f, 50f, 35f });
            #endregion

            this.PrintHeader();
            this.PrintBody();
            _oPdfPTable.HeaderRows = 5;
            _oDocument.Add(_oPdfPTable);
            _oDocument.Close();
            return _oMemoryStream.ToArray();
        }

