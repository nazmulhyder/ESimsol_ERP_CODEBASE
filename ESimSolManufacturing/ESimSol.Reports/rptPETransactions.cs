        public byte[] PrepareReport(List<PETransaction> oPETransactions, Company oCompany, int nReportLayout, string sDateRange)
        {
            _oPETransactions = oPETransactions;
            _oCompany = oCompany;

            #region Page Setup
            _oDocument = new Document(PageSize.A4, 0f, 0f, 0f, 0f);
            _oDocument.SetPageSize(iTextSharp.text.PageSize.A4.Rotate()); //595X842
            _oDocument.SetMargins(20f, 20f, 5f, 20f);
            _oPdfPTable.WidthPercentage = 100;
            _oPdfPTable.HorizontalAlignment = Element.ALIGN_LEFT;

            _oFontStyle = FontFactory.GetFont("Tahoma", 8f, 1);
            _oWriter = PdfWriter.GetInstance(_oDocument, _oMemoryStream);
            _oWriter.ViewerPreferences = PdfWriter.PageModeUseOutlines;
            ESimSolFooter PageEventHandler = new ESimSolFooter();
            PageEventHandler.PrintDocumentGenerator = true;
            PageEventHandler.PrintPrintingDateTime = true;
            _oWriter.PageEvent = PageEventHandler; //Footer print wiht page event handeler            
            _oDocument.Open();
            _oPdfPTable.SetWidths(new float[] { 802 });
            #endregion

            #region Report Body & Header
            if (nReportLayout == 1)//Employee 
            {
                this.PrintHeader("Employee Wise Production Execution", sDateRange);
                this.PrintBody();
                _oPdfPTable.HeaderRows = 4;
            }
           
            else if (nReportLayout == 2)//Date wise
            {
                this.PrintHeader("Date Wise Production Execution", sDateRange);
                this.PrintBodyDateWise();
                _oPdfPTable.HeaderRows = 3;
            }
          
            #endregion

            _oDocument.Add(_oPdfPTable);
            _oDocument.Close();
            return _oMemoryStream.ToArray();
        }

